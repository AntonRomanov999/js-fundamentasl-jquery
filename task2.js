// На HTML-сторінці є посилання <a>. У випадку коли, 
// href починається на https://,
// потрібно додати посиланню атрибут target="_blank".

// Завдання виділене вставко Task2
// Для перевірки запустіть Live Server
// та в командній стрічці введіть команду 
// node tests/task2.test.js

$(function(){
    $("a[href^='https://']").attr('target', '_blank');
})